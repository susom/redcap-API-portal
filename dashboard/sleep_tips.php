<h2>Maintaining a Sleep Routine</h2>
<p>Having a regular sleep schedule, on both weekdays and weekends, can help you sleep better by setting your internal clock. An hour before you sleep, engage in relaxing activities such as reading, meditation, or taking a bath. Avoid high-stress activities such as rigorous exercise or emotionally-charged conversations because they produce cortisol that increases alertness in the body. Staring at the clock can also cause stress.</p>

<h3>Take-away Tips</h3>
<ul>
<li>Maintain the same sleep and wake schedule, on both weekdays and weekends.</li>
<li>Avoid high-stress and stimulating activities before bed.</li>
</ul>

