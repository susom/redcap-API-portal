<h2>Addressing Insomnia and Sleep Apnea</h2>
<h3>Insomnia</h3>
<p>Insomnia is difficulty falling asleep or staying asleep, even when a person has the opportunity to sleep. People with insomnia may experience fatigue, low energy, difficulty concentrating, mood disturbances, and decreased performance in work or at school. There are natural treatments for insomnia that don't involve taking medication.  These treatments are collectively referred to as Cognitive Behavioral Therapy and include approaches such as cognitive therapy, relaxation training, sleep restriction therapy, and stimulus control therapy.  Cognitive therapy involves working with a sleep therapist to identify underlying harmful beliefs you may have about sleep, and replacing those beliefs with ones that have been shown to improve sleep quality.  Relaxation training, sleep restriction therapy, and stimulus control therapy are behavior changes that one can make on their own to help improve sleep, and there are many resources that are freely available online to guide you through them.  Check out some of these resources below.</p>

<h3>Sleep Apnea</h3>
<p>Sometimes we are unaware of the poor quality of our sleep.  Sleep apnea is a common disorder in which you have one or more pauses in breathing or shallow breaths while you sleep, each pause lasting from a few seconds to minutes.  They may occur 30 times or more an hour. Sleep apnea usually disrupts your sleep and will often move you from deep sleep into light sleep, and is the leading cause of excessive daytime sleepiness.  It afflicts 22 million people in the United States, and 80% of those cases are undiagnosed.  If you are regularly feeling sleepy during the day, or your bed partner has indicated that you are snoring loudly or regularly stop breathing during sleep, you may have sleep apnea.  Other indicators are obesity, waking in the night out of breath, or waking in the morning with dry mouth or a headache.  If you suspect you might have sleep apnea, contact a sleep professional in your area to investigate further.  You can use our Find a Sleep Facility tool (http://www.sleepeducation.org/find-a-facility), or visit the American Sleep Apnea Association website (http://www.sleepapnea.org/index.html) to learn more about the condition.</p>

<h3>Take-away Tips</h3>
<ul>
<li>If you are routinely reliant on medications to get to sleep at night, look into addressing your insomnia using more natural methods, such as various cognitive behavioral therapies.</li>
<li>If you regularly experience daytime sleepiness, or your bed partner communicates that you snore loudly or regularly stop breathing during sleep, contact a sleep professional to determine if you might have sleep apnea.</li>
</ul>

