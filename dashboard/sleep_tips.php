<h2>The Ideal Temperature and the Body's Thermoregulation</h2>
<p>Your body temperature will naturally lower during sleep, and is in fact required in order to initiate sleep.  Maintaining a bedroom temperature of around 65 degrees has been shown to be the most favorable for this dip in body temperature.  A warmer room can interfere with your natural thermoregulation, which can lead to restlessness and may even be associated with insomnia.   In the body's attempts to cool off by sweating, you may find yourself, your pajamas and your mattress damp, which can make it difficult to fall asleep or stay asleep.  If the room is too humid, your sweat will not evaporate.  If the room is too cold, you may shiver to increase body temperature, which can also contribute to restless sleep.</p> 
<p>To maintain a cool temperature in the house and bedroom, close the windows and blinds during the day if it is hot outside, and open the windows at night to let in the cool air.  Because heat rises, seek out cooler temperatures for sleep on the lowest floor of the house.  You can use fans to blow the hot air out, or to keep the air flow moving constantly.  In dressing for bed, wear breathable fabric, such as cotton or newer fabrics that can remove moisture can also help to reduce sweating.  Or better yet, sleep in the nude.  Sleeping naked has been shown to allow the body to better regulate its temperature for sleep.</p> 

<h3>Take-away Tips</h3>
<ul>
<li>Maintain bedroom temperature at around 65 degrees</li>
<li>Close the windows and blinds during the day if it is hot outside, and open the windows at night to let in the cool air.</li> 
<li>Sleep on the lowest floor of the house.</li>  
<li>Use fans to blow the hot air out, or to keep the air flow moving constantly.</li> 
<li>In dressing for bed, wear breathable fabric, such as cotton or newer fabrics that can remove moisture and can also help to reduce sweating.  Or sleep in the nude.</li>  
</ul>

