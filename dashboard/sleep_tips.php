<h2>The Bedroom and Bedding</h2>

<h4>Room Design</h4>
<p>Turn your bedroom into a sanctuary for sleep, by creating a sense of peace and calm that can help your body to relax. Important elements include having a space that is clean and free of clutter and distraction. Use colors in the bedroom that you find calming and relaxing. Arrange the furniture or remove unnecessary furniture to simplify the room.  Remove TVs, computers, and other sources of distraction and sources of ambient night-time light.</p>  

<h4>Bedding</h4>
<p>It is important to have a good bed.  Mattress firmness is a personal preference, but your bed and pillow should be comfortable and supportive of your body without making you feel achy and sore the next morning.  Some memory foam materials may trap heat and make it difficult to sleep in the summer.  So, it is important that you find what works for you in your environment.  Most mattresses wear out after 10 years. Consider replacing your mattress if it sags in the middle or at the edges, or if your position shifts when your partner moves. If you are pregnant, lie on your left side if you can because it helps circulation. Replace your pillows when they become shapeless and can no longer support your head and neck. Long pillows that can support your upper body, as well as pillows between your legs, behind your back, or even a thin one under your abdomen can also promote restful sleep.</p>

<h4>Scents</h4>
<p>Your nose is an important factor in restful sleep. Studies have shown that the smell of lavender decreases heart rate and blood pressure, which relaxes you and provides deeper sleep. Most importantly, find a scent that relaxes you.  A few drops of essential oil can be added to the washer when you clean your bedding, or place an essential oil diffuser or potpourri mix in the bedroom.  Use unscented laundry detergents or detergents that have a calming smell, and make an effort to remove offending odors from the bedroom, such as dirty laundry.</p>

<h4>Allergens</h4>
<p>Year-round or seasonal allergies to dust, pollen or mold can interrupt your sleep by causing you to sneeze or sniffle at night. Wash your sheets in hot water once a week and your blankets and pillows regularly. Laundering or drying the pillow at high temperature can help kill dust mites. Clean the surface of your mattress with an upholstery cleaner or sprinkle baking soda to draw out moisture and vacuum it the next day. Vacuum the house regularly and keep your windows closed if you are allergic to pollen. Also consider using an air purifier that removes allergens from the air.</p>

<h3>Take-away Tips</h3>
<ul>
<li>simplify the room by removing unnecessary furniture, clutter and distractions such as TVs and computers.</li>
<li>Use calming colors in bedroom</li>
<li>Have a bed that is comfortable and supportive</li>
<li>Use relaxing scents in the bedroom and on bedding, and remove offending odors from the bedroom.</li>
<li>Reduce allergens in the house by washing bedding, vacuuming the house regularly, and using an air purifier. If you are allergic to pollen, close the windows.</li>
</ul>


