<h2>Sleep Deficiency and its Consequences</h2>
<p>In our fast-paced modern culture, sleep is often the first to be compromised in our effort to meet the competing demands of life.  However, ironically, inadequate sleep substantially hinders our ability to meet those demands.  One night of low quality sleep can result in a number of immediate impacts, including impaired cognitive abilities, slowed reflexes, and increased caloric consumption. In fact, sleep deprivation has similar effects on the body as alcohol.  Being awake for 18 hours straight is similar to having a blood alcohol level of 0.05, and being awake for 24 hours straight is similar to a blood alcohol level of 0.1.  For reference, the legal blood alcohol limit is 0.08.  Drowsy driving has been associated with five to six thousand fatal crashes each year in the U.S..  If you find yourself drowsy driving, immediately pull off the road at a safe place and take a 20-minute nap.  Other options include switching drivers or having a caffeinated beverage.</p> 
<p>Upward of 19% of adults in the U.S. have reported not getting enough sleep everyday, and nearly 40% of adults have reported falling asleep during the day without meaning to at least once a month.  Sleep deficiency can interfere with work or school, and social functioning.  While you sleep your brain forms new pathways involved in learning and memory, and inadequate sleep interferes with these processes, as well as focus, creativity, decision-making, and problem-solving skills.  Sleep deficiency has also been shown to alter brain activity, such that controlling emotions and coping with change is more difficult, as is perception of emotional response in others.  Sleep deprivation has been linked to depression, suicide, and risk-taking behavior.</p>
<p>In the long-run, chronic sleep deficiency has been associated with a number of health problems, including obesity, heart disease, stroke, diabetes, cancer, depression, kidney disease, and high blood pressure.  We are still learning about all the roles that sleep plays in our health, but we know that sleep is critical for the repair of cells, of the heart, and of blood vessels throughout the body.  The role of sleep deficiency in weight gain has also been well established.  Inadequate sleep interferes with hormone balance in the body that controls appetite regulation.  Sleep deprivation increases the hormone ghrelin (tells us when we're hungry) and decreases the hormone leptin (tells us when we're full), making us feel hungrier than when we are well-rested, which can lead to overeating and weight gain.  Sleep is important in blood sugar control, and inadequate sleep can result in higher blood sugar levels and higher risk for diabetes.  Sleep is also required to maintain a healthy immune system, and inadequate sleep can lead to compromised immunity and trouble fighting common infections.</p>  

<h3>How Do You Know if You're Sleep Deficient?</h3>

<p>Experiencing daytime sleepiness can be a good indicator of sleep deficiency.  If you feel like you could fall asleep during normal daytime activities such as these, you may be sleep deficient:</p>
<ul>
<li>Sitting and reading or watching TV</li>
<li>Sitting still in a public place, such as a movie theater, meeting, or classroom</li>
<li>Riding in a car for an hour without stopping</li>
<li>Sitting and talking to someone</li>
<li>Sitting quietly after lunch</li>
<li>Sitting in traffic for a few minutes</li>
</ul>

<p>To better understand your sleep status and hone in on the amount of sleep that your body requires for optimal functioning, try keeping a sleep diary for a couple of weeks. Write down the amount of sleep you get each night, along with how you feel during the day, including level of alertness, mood, and how rested you feel.  You can do your own sleep study and track other factors such as appetite, energy level, athletic performance, and productivity.  Discover the myriad ways that sleep influences your daily experience.</p>

<h3>Who's at Risk for Sleep Deficiency?</h3>

<p>Sleep deficiency can afflict anyone, regardless of age, gender, or ethnicity.  However, certain groups of people may be at higher risk, including:</p>
<ul>
<li>People with schedules that are not well matched to their natural sleep-wake cycle, such as shift workers, first responders, students with early classes, people who travel for work.</li>
<li>People who have limited time for sleep due to being care-takers, working long hours, or working more than one job.</li>
<li>People who intentionally restrict sleep in favor of staying awake to work, study, or participate in other activities.  This is common in student populations, and within workplace cultures that emphasize high productivity. </li>
<li>People who make a lifestyle choice that interferes with sleep, such as abuse of drugs, alcohol, caffeine, or other drugs or medicines to stay awake, or people who do not allow enough time for sleep each night.</li>
<li>People who have an underlying untreated medical problem or condition, such as stress, anxiety, or sleep disorders.</li>
</ul>

<p>If you suspect that you may have an underlying sleep issue, you may want to participate in a sleep study.  A sleep study can allow your doctor to measure the amount of sleep that you get, your quality of sleep, and the presence and severity of an underlying sleep issue.</p>

<h3>Take-away Tips</h3>
<ul>
<li>Don't settle for being sleep deficient.  Sleep is too important to our health and quality of life to ignore or compromise.  If you suspect that you are sleep deficient, take steps to understand the causes and to address them.  Make sleep a priority in your life.  No one else will.</li>
<li>Never drive while drowsy.  If you find yourself driving drowsy, pull over and take a 20-minute nap, switch drivers, or have a cup of coffee or other caffeinated beverage.</li>
<li>If you regularly experience daytime sleepiness, or suspect that you have an underlying sleep issue, contact a sleep professional about participating in a sleep study.</li>
</ul>

