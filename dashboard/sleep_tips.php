<h2>Natural and Artificial Light</h2>
<p>Perhaps you have been amazed at your ability to wake up at the same time every day. Your internal clock, responsible for the sleep-wake cycle, reflects the natural cycles of light and dark. Light influences your behavior, and when you do not receive enough daytime light it interferes with signals to the brain. This disrupts the circadian rhythm and can cause sleepiness, insomnia, and other health problems.  Natural light is important for setting the circadian rhythm. Open your blinds first thing in the morning to let in the light. Try to receive some “solar power” by taking a sun break during the day.</p> 
<p>Minimize your exposure to artificial light in the hours before bed and after dark.  Artificial light and the blue light emitted from electronic screens can influence sleep quality because it suppresses melatonin production. There is some evidence that wearing blue light blocking glasses in the hours before bedtime can help to better prepare the body for sleep.  Also, use dimmer switches to reduce glow from sources of artificial light as bedtime gets closer.  Try to minimize the amount of light in the bedroom, such as from power buttons and streetlamps, because strong artificial light can make it harder to fall asleep.  Use eye-masks, blinds, and curtains to block ambient light.  If you regularly awake in the night to make trips to the bathroom, use a night light instead of a strong overhead light to illuminate the bathroom and pathway.</p>

<h3>Take-away Tips</h3>
<ul>
<li>Open your blinds first thing in the morning to let in the light. </li>
<li>Try to receive some “solar power” by taking a sun break during the day.</li>
<li>Minimize your exposure to electronics in the hours before bed, and to artificial light after dark.</li>
<li>Try wearing blue light blocking glasses in the hours before bedtime </li>
<li>Before you sleep, use dimmer switches to reduce glow from sources of artificial light.</li>
<li>Minimize the amount of light in the room, such as from power buttons and streetlamps</li>
<li>Use eye-masks, blinds, and curtains to block ambient light.</li>
<li>Use night lights instead of strong overhead lights if you need to visit the bathroom during the night.</li>
</ul>



