<h2>When to Exercise or Nap</h2>
<p>Exercising can promote sleep as long as you do it several hours before sleeping and not immediately before going to bed. This is because it stimulates the production of cortisol, which increases your body's alertness and makes it harder to fall asleep.</p>
<p>Late-day naps can also make it more difficult to fall asleep at night.  If you like to take naps, it is best to take short power naps before 5 p.m.</p>   

<h3>Take-away Tips</h3>
<ul>
<li>Finish exercising at least a few hours before bedtime.</li>
<li>If you're a fan of naps, make sure they occur before 5 pm, in order to give your body time to prepare for nighttime sleep.</li>
</ul>

