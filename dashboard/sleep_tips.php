<h2>Diet, Caffeine and Alcohol</h2>

<h3>Diet</h3>
<p>The relationship between food and sleep is complex. The amount of sleep that you get can have an impact on the types of foods that you crave and body weight regulation. Studies have indicated that getting at least 7 hours of sleep at night can make you less likely to crave sweet, salty and starchy junk foods, while sleep restriction can increase the levels of ghrelin in your body, an appetite stimulating hormone, and can cause you to feel hungrier throughout the day.</p>

<p>Conversely, the types of foods that you eat before bedtime have been shown to impact the quality and quantity of sleep that you get.  The amino acid tryptophan may help you to feel drowsier and is contained within many different types of foods, including seeds and nuts, soy, cheese, red meat and poultry, fish, oats, and eggs.  Recent research has suggested that pre-bedtime meals that are also lower in saturated fat, non-fiber carbohydrates including sugar, and higher in fiber are associated with deeper sleep and fewer nocturnal arousals during sleep.</p>  

<p>While certain foods can promote sleep, the best option is to snack lightly if you must eat immediately before bed.  It is best to finish eating dinner hours before bedtime in order to avoid indigestion. Also, avoid excessive drinking before bedtime in order to avoid nighttime trips to the bathroom.</p>

<h3>Caffeine and Alcohol</h3>
<p>Drinks that contain caffeine, found in tea, coffee, and many energy drinks, can decrease your quality of sleep and should be avoided 4-6 hours before going to bed. Avoid drinking alcoholic beverages 3 hours before bed because even though it may initially make you drowsy, it can act as a stimulant and cause you to wake up later during the night.  For quality sleep, it is best to keep alcohol consumption to under 1-2 drinks per day.</p> 

<h3>Take-away Tips</h3>
<ul>
<li>Strive to get at least 7 hours of sleep every night in order to reduce cravings for unhealthy foods.</li>
<li>Tryptophan containing foods may help you to feel drowsier at night.</li>
<li>Having a dinner that is high in fiber and lower in saturated fat, sugar and other non-fiber carbohydrates may contribute to deeper sleep and less sleep disturbances during the night</li>
<li>Stop eating hours before bedtime in order to avoid indigestion.</li>
<li>Avoid excessive fluid consumption immediately before bedtime to avoid nighttime trips to the bathroom.</li>
<li>Avoid caffeine and alcohol intake at least 3 hours before bedtime for alcohol and 4-6 hours before bedtime for caffeine.</li>
</ul>


